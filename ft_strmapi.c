#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	char	*dst;
	size_t	size;

	i = 0;
	size = ft_strlen(s);
	dst = malloc(sizeof(char) * (size + 1));
	if (!dst)
		return (NULL);
	if (!s)
		return (NULL);
	while (s[i])
	{
		dst[i] = f(i, s[i]);
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
