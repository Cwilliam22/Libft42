#include "libft.h"

void	*ft_memcpy(void *restrict dst, const void *restrict src, size_t n)
{
	char		*ptr_dst;
	const char	*ptr_src;

	ptr_dst = (char *)dst;
	ptr_src = (const char *)src;
	if (!src && !dst)
		return (NULL);
	while (n > 0)
	{
		*ptr_dst = *ptr_src;
		ptr_dst++;
		ptr_src++;
		n--;
	}
	return (dst);
}
